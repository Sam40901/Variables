print("hello, this program will convert your height into centimetres and your weight from stones to kilograms.")

inch_height = int(input("how tall are you in inches?: "))

stone_weight = int(input("how much do you weigh in stones?: "))

cm_height= inch_height * 2.54

kg_weight= stone_weight * 6.364

print("your height in centimetres is {0}".format(cm_height))

print("your weight in  kilograms is {0}".format(kg_weight))

